package q542;

public class Main1 {
	public static void main(String[] args) {
//	for 문
		for (int i =10; i<21; i++) {
			System.out.print(i +" ");
		}
		System.out.println();
//	while 문
		int j = 10;
		while (j < 21) {
			System.out.print(j + " ");
			j++;
		}
		System.out.println();
//	do-while 문
		int k = 10;
		do {
			System.out.print(k + " ");
			k++;
		} while (k < 21);
		System.out.println();
	}
}
